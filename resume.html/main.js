// JavaScript for modal image popup

// Get the modal
var modal = document.getElementById("imageModal");

// Get the image element inside the modal
var modalImg = document.getElementById("modalImage");

// Get the <span> element that closes the modal
var span = document.getElementsByClassName("close")[0];

// Add click event listener to each project box
document.querySelectorAll('.project-box').forEach(box => {
  box.addEventListener('click', function() {
    // Show the modal
    modal.style.display = "block";
    // Get the hidden image element inside the clicked project box
    let imgElement = this.querySelector('.project-image');
    // Set the modal image source to the hidden image source
    modalImg.src = imgElement.src;
  });
});

// When the user clicks on <span> (x), close the modal
span.onclick = function() { 
  modal.style.display = "none";
}

// When the user clicks anywhere outside of the modal, close it
window.onclick = function(event) {
  if (event.target == modal) {
    modal.style.display = "none";
  }
}
